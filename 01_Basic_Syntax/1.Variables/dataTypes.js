// 기본 데이터 타입
let string = "Hello, World!"; // 문자열
let number = 42;              // 숫자
let boolean = true;           // 불리언 (true 또는 false)
let nullValue = null;         // null (값이 없음을 나타냄)
let undefinedValue;           // undefined (값이 할당되지 않은 변수)

// 복잡한 데이터 타입: 객체와 배열
let object = { name: "Alice", age: 25 }; // 객체: 속성들의 집합
let array = [1, 2, 3]; // 배열: 순서가 있는 값들의 집합

// 객체와 배열의 사용 예시
console.log(object.name); // Alice 출력
console.log(array[0]); // 1 출력

